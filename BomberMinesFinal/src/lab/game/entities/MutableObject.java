package lab.game.entities;

public class MutableObject {
	
	private int row;
	private int col;
	private int rowX1;
	private int rowX2;
	private int colY1;
	private int colY2;
	/**
	 * @return the row
	 */
	public int getRow() {
		return row;
	}
	/**
	 * @param row the row to set
	 */
	public void setRow(int row) {
		this.row = row;
	}
	/**
	 * @return the col
	 */
	public int getCol() {
		return col;
	}
	/**
	 * @param col the col to set
	 */
	public void setCol(int col) {
		this.col = col;
	}
	/**
	 * @return the rowX1
	 */
	public int getRowX1() {
		return rowX1;
	}
	/**
	 * @param rowX1 the rowX1 to set
	 */
	public void setRowX1(int rowX1) {
		this.rowX1 = rowX1;
	}
	/**
	 * @return the rowX2
	 */
	public int getRowX2() {
		return rowX2;
	}
	/**
	 * @param rowX2 the rowX2 to set
	 */
	public void setRowX2(int rowX2) {
		this.rowX2 = rowX2;
	}
	/**
	 * @return the colY1
	 */
	public int getColY1() {
		return colY1;
	}
	/**
	 * @param colY1 the colY1 to set
	 */
	public void setColY1(int colY1) {
		this.colY1 = colY1;
	}
	/**
	 * @return the colY2
	 */
	public int getColY2() {
		return colY2;
	}
	/**
	 * @param colY2 the colY2 to set
	 */
	public void setColY2(int colY2) {
		this.colY2 = colY2;
	}
}
